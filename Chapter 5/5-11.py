# Ordinal numbers
numeros = [1, 2, 3, 4, 5, 6, 7, 8, 9]

# Loop in the list
for numero in numeros:
    if numero == 1:
        print("1st")
    elif numero == 2:
        print("2nd")
    elif numero == 3:
        print("3rd")
    elif numero == 4:
        print("4th")
    elif numero == 5:
        print("5th")
    elif numero == 6:
        print("6th")
    elif numero == 7:
        print("7th")
    elif numero == 8:
        print("8th")
    elif numero == 9:
        print("9th")
